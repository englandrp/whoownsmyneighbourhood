				<h2 class="anchor" id="a_ownership">How can I find out about land that isn't owned by the council?</h2>
                <p>The <a href="http://http://www.landregistry.gov.uk/" target="_blank">land registry</a> has some information about privately owned land, but Kirklees Council doesn't have any current information.</p>
				<p><a href="/help#main" class="quiet">Top of page</a></p>

				<h2 class="anchor" id="a_landuse">Can we use land owned by the council?</h2>
                <p>The council’s land cannot be used without prior arrangement or agreement. For further information regarding ownership and who to contact for permission to use the land, please email the Terrier team at: prp@kirklees.gov.uk</p>
                <p>Public open spaces and parks are available for the public to enjoy, but even this land is subject to certain restrictions (for example, you’d need to get permission to plant trees or create allotments within a public open space, or to organise a gala in your local park). You can find contact details for parks and open spaces at <a href="http://www.kirklees.gov.uk/parks" target="_blank">http://www.kirklees.gov.uk/parks</a></p>
				<p><a href="/help#main" class="quiet">Top of page</a></p>

				<h2 class="anchor" id="a_contacts">How can I find out who the other community contacts are and the other people registered for updates?</h2>
                <p>Being a community contact is a completely voluntary role and we don't provide the contact details  for these people or those registered for updates to anyone else at all under any circumstances.</p>
                <p>If you want to start working with other people in your area, send a message saying so to the other contacts and suggest meeting on another forum, for example a Facebook group, or through a local organisation. The council might have a community development worker who can help. </p>
                <p>Show some common sense in how you do this and how you use Who Owns My Neighbourhood? in general - don't send personal details through Who Owns My Neighbourhood? and don't be daft about organising meetings - find an organisation to host it, for example.</p>
