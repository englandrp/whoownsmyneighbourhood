				<h2 class="anchor" id="a_started">How do I get started?</h2>
                <p>Put a Kirklees postcode, or the first half of one (eg HD1), into the search box, and click Go.</p>
                <p>That will take you to a map page showing only two different kinds of land.</p>
                <p>Land owned by the council has a red border and shading...</p>
                <p><img src="/x/img/content/councilland.png" alt="council owned land" /></p>
                <p>... all other land doesn't.</p>
                <p><img src="/x/img/content/otherland.png" alt="other land" /></p>
                <p>Click on a plot of land owned by the council and a pop up will appear with a snapshot of information about that plot of land.</p>
                <p>Click on the "View plot details" link to find out more.</p>
				<p><a href="/help#main" class="quiet">Top of page</a></p>

